bb_t ROOK_PEXT_MASK[64];
bb_t BISHOP_PEXT_MASK[64];
bb_t ROOK_PEXT_INDEX[64];
bb_t BISHOP_PEXT_INDEX[64];

bb_t PEXT_TABLE[107648];

bb_t get_edge_filter(bindex_t sq) {
    bb_t result =
        (RANK_MASKS[RANK_1] | RANK_MASKS[RANK_8]) & ~RANK_MASKS[SQUARE_TO_RANK[sq]];
    result |= (FILE_MASKS[FILE_A] | FILE_MASKS[FILE_H]) & ~FILE_MASKS[SQUARE_TO_FILE[sq]];
    return ~result;
}



bb_t generate_sliding_mask(bindex_t sq, const int8_t directions[4][2],
                           bb_t blockers) {
    bb_t moves = 0;
    for (int i = 0; i < 4; i++) {
        int cur_sq = sq;
        while (true) {
            int next_sq = cur_sq + (directions[i][0] + directions[i][1] * 8);

            if (cur_sq % 8 == 0 && next_sq % 8 == 7) { // wrap so stop moving
                break;
            }
            if (cur_sq % 8 == 7 && next_sq % 8 == 0) {
                break;
            }
            if (next_sq >= 64) {
                break;
            }
            if (next_sq < 0) {
                break;
            }

            cur_sq = next_sq; // move and mark bit
            bb_t new_bit = (1ULL << cur_sq);
            moves |= new_bit;
            if (new_bit & blockers) {
                break;
            }
        }
    }
    return moves;
};

void init_pext_table() {
    size_t cur_index = 0;
    for (bindex_t i = 0; i < 64; i++) { // rook moves
        ROOK_PEXT_INDEX[i] = cur_index;
        ROOK_PEXT_MASK[i] = ROOK_SLIDER_MASK[i] & get_edge_filter(i);
        bb_t mask = ROOK_PEXT_MASK[i];

        uint64_t max = 1ULL << __builtin_popcountll(mask);
        for (uint64_t j = 0; j < max; j++) {
            uint64_t blockers = _pdep_u64(j, mask);
            PEXT_TABLE[cur_index++] =
                generate_sliding_mask(i, ROOK_DIRS, blockers);
        }
    }

    for (bindex_t i = 0; i < 64; i++) { // rook moves
        BISHOP_PEXT_INDEX[i] = cur_index;
        BISHOP_PEXT_MASK[i] = BISHOP_SLIDER_MASK[i] & get_edge_filter(i);
        bb_t mask = BISHOP_PEXT_MASK[i];

        uint64_t max = 1ULL << __builtin_popcountll(mask);
        for (uint64_t j = 0; j < max; j++) {
            uint64_t blockers = _pdep_u64(j, mask);
            PEXT_TABLE[cur_index++] =
                generate_sliding_mask(i, BISHOP_DIRS, blockers);
        }
    }
}

bb_t fetch_bishop_moves(bindex_t square, bb_t blockers) {
    size_t index = BISHOP_PEXT_INDEX[square] +
                   _pext_u64(blockers, BISHOP_PEXT_MASK[square]);
    return PEXT_TABLE[index];
}

bb_t fetch_rook_moves(bindex_t square, bb_t blockers) {
    size_t index =
        ROOK_PEXT_INDEX[square] + _pext_u64(blockers, ROOK_PEXT_MASK[square]);
    return PEXT_TABLE[index];
}

void print_bb(bb_t bboard) {
    for (int rank = 0; rank < 8; rank++) {
        for (int file = 7; file >= 0; file--) {
            uint64_t mask = 1ULL << (63 - (rank * 8) - file);
            char ch = (mask & bboard) != 0 ? 'X' : '.';
            printf("%c ", ch);
        }
        printf("\n");
    }
}

void print_pieces(piece_t *board) {
    for (int rank = 7; rank >= 0; rank--) {
        printf("%d ", rank + 1);
        for (int file = 0; file < 8; file++) {
            char ch;
            switch (board[rank * 8 + file]) {
            case PIECE_WPAWN:
                ch = 'P';
                break;
            case PIECE_WKNIGHT:
                ch = 'N';
                break;
            case PIECE_WBISHOP:
                ch = 'B';
                break;
            case PIECE_WROOK:
                ch = 'R';
                break;
            case PIECE_WQUEEN:
                ch = 'Q';
                break;
            case PIECE_WKING:
                ch = 'K';
                break;

            case PIECE_BPAWN:
                ch = 'p';
                break;
            case PIECE_BKNIGHT:
                ch = 'n';
                break;
            case PIECE_BBISHOP:
                ch = 'b';
                break;
            case PIECE_BROOK:
                ch = 'r';
                break;
            case PIECE_BQUEEN:
                ch = 'q';
                break;
            case PIECE_BKING:
                ch = 'k';
                break;

            case PIECE_NONE:
                ch = '.';
                break;

            default:
                ch = '?';
                break;
            }
            printf("%c ", ch);
        }
        printf("\n");
    }

    printf("  A B C D E F G H\n");
}
